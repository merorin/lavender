class GarageDoor {

    fun up() {
        println("GarageDoor is open...")
    }

    fun down() {
        println("GarageDoor is closed...")
    }

    fun stop() {
        println("GarageDoor is stopped...")
    }

    fun lightOn() {
        println("GarageDoor is light-on...")
    }

    fun lightOff() {
        println("GarageDoor is light-off...")
    }
}